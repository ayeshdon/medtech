package com.hseya.medtech.jobcard.bean;

public class CustomerBean {

	
	private String cus_ID;
	private String cus_name;
	private String contactNo;
	private String contactPerson;
	private String email;
	private String persontoContanct;
	
	
	
	
	public String getCus_ID() {
		return cus_ID;
	}
	public void setCus_ID(String cus_ID) {
		this.cus_ID = cus_ID;
	}
	public String getCus_name() {
		return cus_name;
	}
	public void setCus_name(String cus_name) {
		this.cus_name = cus_name;
	}
	public String getContactNo() {
		return contactNo;
	}
	public void setContactNo(String contactNo) {
		this.contactNo = contactNo;
	}
	public String getContactPerson() {
		return contactPerson;
	}
	public void setContactPerson(String contactPerson) {
		this.contactPerson = contactPerson;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPersontoContanct() {
		return persontoContanct;
	}
	public void setPersontoContanct(String persontoContanct) {
		this.persontoContanct = persontoContanct;
	}
	
	
	
	
	
	
}
