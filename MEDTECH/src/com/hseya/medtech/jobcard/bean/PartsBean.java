package com.hseya.medtech.jobcard.bean;

public class PartsBean {

	
	private String partName;
	private String ID;
	private String partQuantitys;
	private String partID;
	private String flag;
	
	
	
	public String getPartName() {
		return partName;
	}
	public void setPartName(String partName) {
		this.partName = partName;
	}
	public String getPartQuantitys() {
		return partQuantitys;
	}
	public void setPartQuantitys(String partQuantitys) {
		this.partQuantitys = partQuantitys;
	}
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public String getFlag() {
		return flag;
	}
	public void setFlag(String flag) {
		this.flag = flag;
	}
	public String getPartID() {
		return partID;
	}
	public void setPartID(String partID) {
		this.partID = partID;
	}
	
	
	
}
